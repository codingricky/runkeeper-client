package com.github.codingricky.runkeeperclient.model;

public class TeamFeedItem {
    private long userID;
    private String name;
    private String url;
    private String profile;

    public long getUserID() {
        return userID;
    }

    public String getName() {
        return name;
    }

    public String getUrl() {
        return url;
    }

    public String getProfile() {
        return profile;
    }
}
